num1 = 11 # Declaring the num1 as Global variable
num2 = 6 # Declaring the num1 as Global variable

def addition(): # declaring the addition function
    addition = num1 + num2
    print("Addition of num1 and num2 is", addition)

def sub(): # declaring the subtraction function
    sub = num1 - num2
    print("Subtraction of num1 and num2 is", sub)

# Invoking the below functions
addition()
sub()